def get_piece_position_value(piece_type, square, color):
    rank = chess.square_rank(square)
    file = chess.square_file(square)
    # Si es negro, invertir la matriz
    if color == chess.BLACK:
        rank = 7 - rank
    if piece_type == chess.PAWN:
        return PAWN_POSITION_VALUES[rank][file]
    elif piece_type == chess.KNIGHT:
        return KNIGHT_POSITION_VALUES[rank][file]
    elif piece_type == chess.BISHOP:
        return BISHOP_POSITION_VALUES[rank][file]
    elif piece_type == chess.ROOK:
        return ROOK_POSITION_VALUES[rank][file]
    elif piece_type == chess.QUEEN:
        return QUEEN_POSITION_VALUES[rank][file]
    elif piece_type == chess.KING:
        return KING_POSITION_VALUES[rank][file]
    return 0

def evaluate_board(board: chess.Board, color: chess.Color):
    score = 0
    for square in chess.SQUARES:
        piece = board.piece_at(square)
        if piece:
            value = PIECE_VALUES.get(piece.piece_type, 0)
            pos_value = get_piece_position_value(piece.piece_type, square, piece.color)
            if piece.color == color:
                score += value + pos_value
            else:
                score -= value + pos_value
    return score

class HeuristicChessAI(ChessAI):
    def select_move(self, board: chess.Board, color: chess.Color):
        best_score = None
        best_move = None
        for move in board.legal_moves:
            board.push(move)
            score = evaluate_board(board, color)
            board.pop()
            if best_score is None or score > best_score:
                best_score = score
                best_move = move
        return best_move

